/**
 * 获取边框色类名
 * @param color 边框色类型
 * @returns 边框色CSS类名
 */
export function getContentBorderClass(color?: ContentBorderColor): string {
    if (!color) return 'cosy:border-black/10'; // 默认边框色
    return allContentBorderClasses[color] || 'cosy:border-black/10';
}
